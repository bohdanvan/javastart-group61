/**
 * @author bvanchuhov
 */
public class StraightRangePrinter {

    public static void main(String[] args) {
        int from = 0;
        int to = 15;

        for (int n = from; n <= to; n++) {
            System.out.print(n + " ");
        }
        System.out.println();
    }
}
