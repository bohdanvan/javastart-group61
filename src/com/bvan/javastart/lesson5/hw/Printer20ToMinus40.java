/**
 * @author bvanchuhov
 */
public class Printer20ToMinus40 {

    public static void main(String[] args) {
        // n = 20...-40
        for (int n = 20; n >= -40; n--) {
            System.out.print(n + " ");
        }
        System.out.println();
    }
}
