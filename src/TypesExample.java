public class TypesExample {

    public static void main(String[] args) {
        // Integer Types
        byte b = 100;
        short s = 10000;
        int x = 1000000000;
        long l = 1000000000000000000L;

        // Floating point
        float f = 10.5f;
        double d = 10.3523234213;

        // Character
        char c = 'a';

        // Logical
        boolean bool = true;

        // Object Types
        String str = "Hello";
    }
}
